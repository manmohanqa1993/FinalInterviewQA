package NUMBERS;

public class Swap_2_Numbers_withot_3rd_Value {
	public static void main(String[] args) {
		int a=10;
		int b=20;
		System.out.println("a value is "+a);
		System.out.println("b value is "+b);
		a=a+b;
		b=a-b;
		a=a-b;
		System.out.println("After reversing..");
		System.out.println("a value is "+a);
		System.out.println("b value is "+b);

	}

}
