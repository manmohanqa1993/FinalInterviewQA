package NUMBERS;

public class Swap_2_Numbers_with_3rd_Value {
	public static void main(String[] args) {
		int a=10;
		int b=20;
		System.out.println("Value of a is "+a);
		System.out.println("Value of b is "+b);
		int temp;
		temp=a;
		a=b;
		b=temp;
		System.out.println("After reversing..");
		System.out.println("Value of a is "+a);
		System.out.println("Value of b is "+b);
	}

}
